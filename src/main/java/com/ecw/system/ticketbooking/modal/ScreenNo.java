package com.ecw.system.ticketbooking.modal;


public class ScreenNo {
    private String screenNo;

    public ScreenNo(String screenNo) {
        this.screenNo = screenNo;
    }

    @Override
    public String toString() {
        return "ScreenNo{" +
                "id='" + screenNo + '\'' +
                '}';
    }
}
